#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*s;
	int		i;

	s = "";
	i = 0;
	while (n >= 0)
	{
		s[i++] = n % 10 + '0';
		n /= 10;
	}
	s[i] = 0;
	write(fd, s, 1);
}
