#include <stdio.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ss;

	ss = s;
	i = 0;
	while (i < n)
	{
		printf("ss[%zu] ==> %c\n", i, ss[i]);
		ss[i] = 0;
		i++;
	}
}
